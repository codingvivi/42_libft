#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2 && n < i)
	{
		i++;
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
